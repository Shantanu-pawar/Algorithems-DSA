package com.shantu.ch2_loops_and_JumpStatements;

public class ex1_print_Table_Of_7 {
    public static void main(String[] args) {

//        jump statements
//        print table of 5

        int x = 5 ;
        for (int i = 1 ; i <= 10 ; i++) {

            System.out.println(x + " ");
            x = x + 5;

        }

    }
}
